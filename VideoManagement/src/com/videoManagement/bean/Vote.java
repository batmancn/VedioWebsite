package com.videoManagement.bean;

/**
 * Vote entity. @author MyEclipse Persistence Tools
 */

public class Vote implements java.io.Serializable {

	// Fields

	private Integer id;
	private Resources resources;
	private String options;
	private String grade;
	private String voteIp;

	// Constructors

	/** default constructor */
	public Vote() {
	}

	/** minimal constructor */
	public Vote(Integer id, String options) {
		this.id = id;
		this.options = options;
	}

	/** full constructor */
	public Vote(Integer id, Resources resources, String options, String grade,
			String voteIp) {
		this.id = id;
		this.resources = resources;
		this.options = options;
		this.grade = grade;
		this.voteIp = voteIp;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Resources getResources() {
		return this.resources;
	}

	public void setResources(Resources resources) {
		this.resources = resources;
	}

	public String getOptions() {
		return this.options;
	}

	public void setOptions(String options) {
		this.options = options;
	}

	public String getGrade() {
		return this.grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public String getVoteIp() {
		return this.voteIp;
	}

	public void setVoteIp(String voteIp) {
		this.voteIp = voteIp;
	}

}